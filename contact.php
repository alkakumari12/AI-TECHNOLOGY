// Function to validate email domains
function validateEmailDomain(email) {
    // Regular expression to match allowed domains
    var pattern =
        /@(gmail\.com|yahoo\.com|outlook\.com|protonmail\.com|icloud\.com|info\.com|co\.in|zoho\.com|omeie\.com|example\.com)$/i;
    return pattern.test(email);
}

// Add an event listener to the email input field
document.getElementById("email").addEventListener("input", function() {
    var emailInput = this.value.trim();
    var errorMessage = document.getElementById("email-error");

    // Validate email domain
    if (!validateEmailDomain(emailInput)) {
        // errorMessage.innerHTML = "Only gmail.com, yahoo.com, outlook.com, protonmail.com, icloud.com, info.com, co.in, zoho.com, omeie.com, and example.com email addresses are allowed.";
        this.setCustomValidity("Invalid email domain.");
    } else {
        errorMessage.innerHTML = "";
        this.setCustomValidity("");
    }
});
